class Calculator():
    def addition(x, y):
        return x + y
    
    def subtraction(x, y):
        return x - y
    
    def divide(x, y):
        return x / y
    
    def modulus(x, y):
        return x % y
    

num = Calculator

print(num.addition(2, 2))
print(num.subtraction(210, 2))
print(num.divide(10,5))
print(num.modulus(10, 5))



